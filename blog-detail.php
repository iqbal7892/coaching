						<div class="entry-step">
							<h4>They are passionate</h4>
							<div class="textwidget-para">
								<p>A successful life coach is passionate about life and what a person can do with it. This shows in how they handle the people they are guiding and how they live their own lives. They are unafraid of showing a person how to become who they want to be and by sharing knowledge that will help them get to where they want to go. This passion shows when they talk, when they listen, when they ask questions, and when they show people what they are.</p>	
							</div>
						</div>
						<div class="entry-step">
							<h4>They lead by example</h4>
							<div class="textwidget-para">
								<p>One of the surefire ways to inspire people to succeed is to show them that you are a success, and this is what great coaches do. They show that they have succeeded in achieving their goals in life and are sharing the secrets to their success through coaching. Leading by example is what most successful coaches do and this can be seen in the experiences that they share with people, the ideas that they impart, and the lessons that they talk about.</p>	
							</div>
						</div>
						<div class="textwidget-para">
							<p>These ten habits are not that easy to cultivate but they can be practiced and can become part of who you are over time. If you want to succeed as a life coach, these are habits that need to be part of your life, day in and day out. Successful coaches did not get to where they are in an instant. They had to mold who they are over time and cultivated these habits to get to where they are right now.</p>
							<p>Other habits you also need to include in your life to become a successful coach are the habits of constantly learning as well as being able to know when to talk and when to listen.</p>
